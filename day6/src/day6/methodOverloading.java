package day6;


	public class methodOverloading{
		//method 1- 2 args of int datatype with int return type
		public static int add(int a , int b){
			return a+b;
			}
		
		//method  2- 
		public static float add(float a, float b){
			return a+b;
		}
		//method 3- 3 args
		public static int add(int a, int b, int c){
			return a+b+c;
			}
	}
